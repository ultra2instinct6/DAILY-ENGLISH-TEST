const DAILY_LIKE_COUNT = 100;
// Pick a deterministic 100-item slice of the vocabulary bank for today's test.
// Then group consecutive items by category so the UI shows tidy sections.
const todayLikeChecklist = (() => {
  const picked = getDailyChecklist(
    likeChecklistBank,
    getTodayDateString(),
    DAILY_LIKE_COUNT,
  );
  // Stable group-by-category preserving the daily-shuffled order within groups.
  const seen = new Map<string, typeof picked>();
  picked.forEach((item) => {
    const key = item.category ?? 'Items';
    if (!seen.has(key)) seen.set(key, []);
    seen.get(key)!.push(item);
  });
  return Array.from(seen.values()).flat();
})();

